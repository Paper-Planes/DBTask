package vo;

public class policyholder {
    private String phNumber;//账号
    private String phPwd;//密码
    private String phName;//姓名
    private String phID;//身份证号
    private String phPhone;//电话
    private String phAddr;//地址

    public void setPhNumber(String phNumber) {
        this.phNumber = phNumber;
    }

    public String getPhNumber() {
        return phNumber;
    }

    public void setPhPwd(String phPwd) {
        this.phPwd = phPwd;
    }

    public String getPhPwd() {
        return phPwd;
    }

    public void setPhName(String phName) {
        this.phName = phName;
    }

    public String getPhName() {
        return phName;
    }


    public void setPhID(String phID) {
        this.phID = phID;
    }

    public String getPhID() {
        return phID;
    }

    public void setPhPhone(String phPhone) {
        this.phPhone = phPhone;
    }

    public String getPhPhone() {
        return phPhone;
    }

    public void setPhAddr(String phAddr) {
        this.phAddr = phAddr;
    }

    public String getPhAddr() {
        return phAddr;
    }
}
